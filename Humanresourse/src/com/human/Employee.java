    private void submitTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitTActionPerformed
        // TODO add your handling code here:
        if(conn != null){
            String tName = nameT.getText();
            int tId = Integer.parseInt(idT.getText());
            String tPro = proT.getText();
            String tTraning = traningT.getText();
            String tSkill = skillT.getText();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            String tStart = sdf.format(startT.getDate());
            String tEnd = sdf.format(endT.getDate());
        String sql = "INSERT INTO traningdepartments (Name,EmployeeId,ProjectName,TraningDepartment,SkillReport,StartDate,EndDate) values(?,?,?,?,?,?,?);";
        
        try {
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, tName);
                pst.setInt(2, tId);
                pst.setString(3, tPro);
                pst.setString(4, tTraning);
                pst.setString(5, tSkill);
                pst.setString(6, tStart);
                pst.setString(7, tEnd);               
                
                boolean rs = pst.execute();
                if(!rs){
                   JOptionPane.showMessageDialog(this, "New Row Added.");  
                }else{
                    JOptionPane.showMessageDialog(this, "Failed"); 
                }
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            }
        }else{            
           JOptionPane.showMessageDialog(this, "connection failed"); 
        }
    }//GEN-LAST:event_submitTActionPerformed

